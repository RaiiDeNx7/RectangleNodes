class Point {
    float x, y;

    /**
     * Description: Initializes a new instance of the Point class with specified x and y coordinates.
     * 
     * @param x (float): The x-coordinate of the point.
     * @param y (float): The y-coordinate of the point.
     */
    // Add a method to access the root node
    Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
}